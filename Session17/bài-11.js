
console.log(Math.abs(-5.5)); 
// Kết quả là 5.5 vì Math.abs() trả về giá trị tuyệt đối của số âm.
console.log(Math.abs(10)); 
// Kết quả là 10 vì số dương không thay đổi.

console.log(Math.ceil(4.4));
 // Kết quả là 5 vì Math.ceil() làm tròn lên số nguyên gần nhất.
console.log(Math.ceil(5.8)); 
// Kết quả là 6 vì làm tròn lên.

console.log(Math.floor(5.7)); 
// Kết quả là 5 vì Math.floor() làm tròn xuống số nguyên gần nhất.
console.log(Math.floor(6.1)); 
// Kết quả là 6 vì làm tròn xuống.

console.log(Math.round(10.1)); 
// Kết quả là 10 vì Math.round() làm tròn theo quy tắc: nhỏ hơn 0.5 thì làm tròn xuống.
console.log(Math.round(20.6)); 
// Kết quả là 21 vì 0.6 >= 0.5 nên làm tròn lên.